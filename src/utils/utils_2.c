int	whath_is(char ***a, int *i)
{
	if (a[0][*i][0] == '>' && a[0][*i + 1] && a[0][*i + 1][0] != '<'
		&& a[0][*i + 1][0] != '>')
		return (1);
	else if (a[0][*i][0] == '<' && a[0][*i + 1] && a[0][*i + 1][0] != '>'
		&& a[0][*i + 1][0] != '<' )
		return (2);
	else if (a[0][*i][0] == '>' && a[0][*i + 1] && a[0][*i + 1][0] == '>')
		return (3);
	else if (a[0][*i][0] == '<' && a[0][*i + 1] && \
		a[0][*i + 1][0] == '<')
		return (4);
	else if (a[0][*i][0] != '|')
		return (5);
	else
		return (0);
}
